x = 5
y = 4

# addition
result = x + y
print(result)  # Output: 9
# subtraction
result = x - y
print(result)  # Output: 1
# multiplication
result = x * y
print(result)  # Output: 20
# division
result = x / y
print(result)  # Output: 1.25
# modulus
result = x % y
print(result)  # Output: 1
# exponentiation
result = x ** y
print(result)  # Output: 625
# floor division
result = x // y
print(result)  # Output: 1
